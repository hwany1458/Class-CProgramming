#include <stdio.h>

int main(void){
    // 세로 5줄, 가로 5칸짜리 지도 선언 및 초기화
    int map[5][5] = {
        {1, 1, 1, 1, 1},  // [0]층 (맨 윗줄)
        {1, 0, 0, 0, 1},  // [1]층
        {1, 0, 9, 0, 1},  // [2]층 (가운데 9는 플레이어!)
        {1, 0, 0, 0, 1},  // [3]층
        {1, 1, 1, 1, 1}   // [4]층 (맨 아랫줄)
    };

    printf("=== [ 지하 던전 지도 ] ===\n\n");

    // 이중 반복문 (Nested Loop)
    // 바깥쪽 for문: 세로(y) 방향으로 한 층씩 내려감
    for (int y = 0; y < 5; y++) 
    {
        // 안쪽 for문: 가로(x) 방향으로 방을 하나씩 훑음
        for (int x = 0; x < 5; x++) 
        {
            if (map[y][x] == 1) printf("■ ");      // 1은 벽
            else if (map[y][x] == 0) printf("  ");  // 0은 길
            else if (map[y][x] == 9) printf("옷 "); // 9는 플레이어
        }
        printf("\n"); // 한 층(가로줄) 출력이 끝나면 줄바꿈!
    }

    return 0;
}