#include <stdio.h>

int main(void) {
    // 1. 100칸짜리 인벤토리 선언 (아직 값은 쓰레기 값일 수 있음)
    int inven[100]; 

    // 2. 테스트를 위해 앞쪽 3개만 값을 넣어봅시다.
    inven[0] = 10;
    inven[1] = 20;
    inven[2] = 30;

    // 나머지는 값을 안 넣었으므로 이상한 숫자가 나올 수 있습니다.
    printf("=== [ 전체 인벤토리 스캔 ] ===\n");

    // 3. 반복문과 배열의 결합 (자동화)
    // i는 0부터 시작해서 99(100 미만)까지 1씩 커집니다.
    for (int i = 0; i < 100; i++) 
    {
        // inven[0], inven[1] ... inven[99] 까지 자동으로 변함!
        printf("%d번 칸 아이템 개수: %d\n", i, inven[i]);
    }
    return 0;
}