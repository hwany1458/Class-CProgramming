#include <stdio.h>

int main(void) {
    int input;

    printf("=== [ 숫자 검사기 작동 ] ===\n");

    do {
        // [1] 일단 사용자에게 입력을 받습니다. (조건 검사 전!)
        printf("10 이상의 숫자를 입력하세요: ");
        scanf("%d", &input);

        // 사용자가 5를 입력했다면? -> 아직 조건 검사 전이라 실행됨.

    } while (input < 10); 
    // [2] 이제 검사합니다. 
    // 입력값이 10보다 작으면(참이면) -> 다시 do로 올라가서 재입력 요구!
    // 입력값이 10 이상이면(거짓이면) -> 반복 종료.

    printf("통과! 입력한 숫자: %d\n", input);
    
    return 0;
}
