#include <stdio.h>

int main(void) {

    printf("=== [ 자동 수련 시스템 가동 ] ===\n");

    // 기계 세팅: 1부터 시작, 100까지, 1씩 증가
    // 여기서 'i'는 'iterator(반복자)'의 약자로 관습적으로 많이 씁니다.
    for (int i = 1; i <= 100; i++) 
    {
        printf("[%d회차] 허수아비를 공격했습니다!\n", i);
        // 이 안에서는 변수 i를 현재 몇 번째인지 확인하는 용도로 쓸 수 있습니다.
    }

    printf("=== [ 훈련 종료 ] ===\n");
    printf("교관: 수고했다. 정확히 100번 쳤군.\n");
    return 0;
}
